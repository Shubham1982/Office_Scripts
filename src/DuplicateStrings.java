import java.util.*;

public class DuplicateStrings {
    public static void main(String[] args) {
        List<String> list = Arrays.asList(
                "4145","7775","12902","8370","2055","2055","13061","7686","4665","9509","2701","9685","2564","12902","2564","4667","7936","9324","5327","14720","7029","9012","3380","1893","9324","7775","8009","10488","13726","13061","8612","7625","7936","14720","13001","1893","3380","13762","8612","8370","2701","9685","10875","2566","32279","32287","32245","32244","5327","9821","14736","1534","719","6546","12902","5379","6681","13888","509","7936","8370","3380","32244","32245","8932","8565","6706","6620","8411","12902","8370","9685","1893","4256","27397","719","11852","4665","26017","33710","1534","2701","2564","32245","45825","45824","45823","45826","3380","32244","1260","9821","2795","1372","9509","9064","1534","8932","43230","43230","46781","46782","9267","2070","47122","32245","32244"
        );

        Map<String, Integer> frequencyMap = new HashMap<>();

        // Count occurrences
        for (String str : list) {
            frequencyMap.put(str, frequencyMap.getOrDefault(str, 0) + 1);
        }

        System.out.println("Duplicate strings:");
        for (Map.Entry<String, Integer> entry : frequencyMap.entrySet()) {
            if (entry.getValue() > 1) {
                System.out.println(entry.getKey() + " (repeated " + entry.getValue() + " times)");
            }
        }
    }
}
